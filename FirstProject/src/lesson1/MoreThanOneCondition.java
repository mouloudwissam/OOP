package lesson1;

public class MoreThanOneCondition {
	public static void main (String[] args) {
	int A=20;
	if (A!=0 && A==26) {
		System.out.println(" The Best Answer Is One");
	} 
	else if (A>=0 && A>=26) { 
		System.out.println(" The Answer Is Two");
	}
	else {
		System.out.println("Yes The Best");
	}
	}
}
