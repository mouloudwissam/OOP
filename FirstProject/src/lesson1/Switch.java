package lesson1;

public class Switch {
	public static void main(String[] args) {
		int X=3;
		switch (X) {
		case 1:
			System.out.println("the value of x 1");

			break;
		case 2:
			System.out.println("the value of x is 2");
			break;
		case 3:
			System.out.println("the value of x is 3");
			break;
		default:
			System.out.println(" the value  x is not one of them");

		}
	}

}
