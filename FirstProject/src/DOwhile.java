
public class DOwhile {


	public static void main(String[] args) {
		int C=12;
		int B=13;
		int W=17;
		int R=38;
		int O=30;
		do {System.out.println(C);
		C++;
		}
		while(C<20);
		System.out.println("************************");

		do {System.out.println(B);
		C++;
		}
		while (B==20);	
		System.out.println("%%%%%%%%");

		do{ System.out.println(W);
		C++;
		}
		while(W==18);
		System.out.println("@@@@@@@@@@");


		do {System.out.println(R);
		R++;
		System.out.println();
		}

		while (R> 13);

		do {System.out.println(O);
		O++;
		}
		while ( O>30 && O<50);
	}	
}
